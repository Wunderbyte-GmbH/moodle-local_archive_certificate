/**
 * Post installation procedure
 */
function xmldb_local_archive_certificate_install(): bool {
    global $CFG;
    // Create default archive directory
    $defaultpath = $CFG->dataroot . '/certarchive';
    if (!is_dir($defaultpath)) {
        mkdir($defaultpath, 0755, true);
    }

    return true;
}
